function movePlayers(positions, names) {
    for (const [id, position] of Object.entries(positions)) {
        const player = document.getElementById(id);
        if (player) {
            player.style.left = position.left;
            player.style.top = position.top;
            player.textContent = names[id];
        }
    }
}

function op_movePlayers(positions, names) {
    for (const [id, position] of Object.entries(positions)) {
        const player = document.getElementById(id);
        if (player) {
            player.style.right = position.right;
            player.style.top = position.top;
            player.textContent = names[id];
        }
    }
}

function moveBall(positions) {
    for (const [id, position] of Object.entries(positions)) {
        const player = document.getElementById(id);
        if (player) {
            player.style.left = position.left;
            player.style.top = position.top;
        }
    }
}

document.getElementById('changeSetpiece').addEventListener('click', () => {
    const selector = document.getElementById('setpieceSelector');
    selector.style.display = selector.style.display === 'none' ? 'block' : 'none';
});

document.getElementById('setpieceSelector').addEventListener('change', (event) => {
    const setpiece = event.target.value;
    if (setpieces[setpiece]) {
        movePlayers(setpieces[setpiece].positions, setpieces[setpiece].names);
        op_movePlayers(setpieces[setpiece].positions, setpieces[setpiece].names);
    }
});
